package exampletest.classExtends02;

public class Point {
    private int x;
    private int y;

    public Point() {
    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void showPoint() {
        System.out.println("Point 생성자 호출");
        System.out.println("(" + x + "," + y + ")");
    }

}
